import { execFileSync } from "node:child_process";
import fs from "node:fs";
import path from "node:path";
import { fileURLToPath } from "node:url";

const scriptPath = fileURLToPath(import.meta.url);
const root = path.resolve(path.dirname(scriptPath), "..");
const outputPath = path.join(root, "src", "app", "generated", "buildInfo.js");

function runGit(args) {
  try {
    return execFileSync("git", args, {
      cwd: root,
      encoding: "utf8",
      stdio: ["ignore", "pipe", "ignore"],
      windowsHide: true,
    }).trim();
  } catch {
    return "";
  }
}

function readPackageVersion() {
  try {
    const raw = fs.readFileSync(path.join(root, "package.json"), "utf8");
    return JSON.parse(raw).version || "0.0.0";
  } catch {
    return "0.0.0";
  }
}

export function writeBuildInfo() {
  const buildInfo = {
    version: readPackageVersion(),
    commit: runGit(["rev-parse", "HEAD"]),
    shortCommit: runGit(["rev-parse", "--short", "HEAD"]),
    committedAt: runGit(["show", "-s", "--format=%cI", "HEAD"]),
  };

  const file = `// Generated by scripts/write-build-info.mjs. Do not edit by hand.
export const BUILD_INFO = ${JSON.stringify(buildInfo, null, 2)};

export default BUILD_INFO;
`;

  fs.mkdirSync(path.dirname(outputPath), { recursive: true });
  fs.writeFileSync(outputPath, file, "utf8");
  console.log(
    `Wrote build info v${buildInfo.version}${buildInfo.shortCommit ? ` (${buildInfo.shortCommit})` : ""}`
  );
}

if (process.argv[1] && path.resolve(process.argv[1]) === scriptPath) {
  writeBuildInfo();
}
